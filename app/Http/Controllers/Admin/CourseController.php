<?php

namespace App\Http\Controllers\Admin;

use App\College;
use App\Course;
use App\DataTables\CourseDataTable;
use App\Http\Controllers\Controller;
use Illuminate\Http\Request;

class CourseController extends Controller
{
    public function __construct()
    {
        $this->middleware('permission:read_courses')->only(['index']);
        $this->middleware('permission:create_courses')->only(['create','store']);
        $this->middleware('permission:update_courses')->only(['edit','update']);
        $this->middleware('permission:delete_courses')->only(['destroy']);
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(CourseDataTable $dataTable)
    {
        return $dataTable->render('admin.courses.index');
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $colleges=College::all();
        return view('admin.courses.create', compact('colleges'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=> 'required',
            'college_id'=>'required'
        ]);
        Course::create([
            'name'=>$request->name,
            'college_id'=>$request->college_id
        ]);

        return redirect()->route('admin.courses.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {

        $courses=Course::find($id);
        $colleges=College::all();
        return view('admin.courses.edit', compact('courses','colleges'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'name'=> 'required',
        ]);
        $course=Course::find($id);
        $course->update([
            'name'=>$request->name
        ]);

        return redirect()->route('admin.courses.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {

        $course=Course::find($id);
        if($course){
            $course->delete();
        }
        return redirect()->route('admin.courses.index');
    }
}
